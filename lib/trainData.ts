// Helper function to get station by ID
export const getStationById = (id: string): Station | undefined => {
  return stations.find(station => station.id === id);
};

// Helper function to get station name
export const getStationName = (id: string): string => {
  const station = getStationById(id);
  return station ? station.name : id;
};
